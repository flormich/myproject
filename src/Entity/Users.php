    // public function setPseudo(?string $username): self
    // {
    //     $this->username = $username;

    //     return $this;
    // }

    public function getName(): ?string
    {
        return $this->name;
    }

    public function setName(?string $name): self
    {
        $this->name = $name;

        return $this;
    }

    public function getFirstname(): ?string
    {
        return $this->firstname;
    }

    public function setFirstname(?string $firstname): self
    {
        $this->firstname = $firstname;

        return $this;
    }

    public function getRole(): ?Roles
    {
        return $this->role;
    }

    public function setRole(?Roles $role): self
    {
        $this->role = $role;

        return $this;
    }

    public function getDateCreate(): ?\DateTimeInterface
    {
        return $this->dateCreate;
    }

    public function setDateCreate(\DateTimeInterface $dateCreate): self
    {
        $this->dateCreate = $dateCreate;

        return $this;
    }

    public function getDateConnect(): ?\DateTimeInterface
    {
        return $this->dateConnect;
    }

    public function setDateConnect(\DateTimeInterface $dateConnect): self
    {
        $this->dateConnect = $dateConnect;

        return $this;
    }


    public function initRole($em) {
 
     
        // Initialise le role par defaut (identifiant 1 en bdd)
        $role = $em->getRepository(Roles::class)->find(2);
        $this->role = $role;
     
        // Retourne l'instance de l'objet (fluent)
        return $this;
    }
}
